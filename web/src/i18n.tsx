export type AppLocale = 'en' | 'zh-CN'
export type LocalePreference = 'auto' | AppLocale

export const LOCALE_STORAGE_KEY = 'admin-locale-preference'

export function resolveLocale(preference: LocalePreference, browserLanguage?: string): AppLocale {
  if (preference !== 'auto') {
    return preference
  }

  const language =
    browserLanguage ??
    (typeof navigator !== 'undefined' ? navigator.language : 'en')

  return language.toLowerCase().startsWith('zh') ? 'zh-CN' : 'en'
}


export function loadLocalePreference(): LocalePreference {
  if (typeof window === 'undefined') {
    return 'auto'
  }

  const value = window.localStorage.getItem(LOCALE_STORAGE_KEY)
  if (value === 'en' || value === 'zh-CN' || value === 'auto') {
    return value
  }
  return 'auto'
}


export function saveLocalePreference(preference: LocalePreference): void {
  if (typeof window === 'undefined') {
    return
  }

  window.localStorage.setItem(LOCALE_STORAGE_KEY, preference)
}


